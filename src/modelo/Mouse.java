package modelo;

public class Mouse {

	private String tipoMouse;
	private String tipoConexion;
	private String dpiMaximo;
	private String numMacros;
	private String pesas;
	public String getTipoMouse() {
		return tipoMouse;
	}
	public void setTipoMouse(String tipoMouse) {
		this.tipoMouse = tipoMouse;
	}
	public String getTipoConexion() {
		return tipoConexion;
	}
	public void setTipoConexion(String tipoConexion) {
		this.tipoConexion = tipoConexion;
	}
	public String getDpiMaximo() {
		return dpiMaximo;
	}
	public void setDpiMaximo(String dpiMaximo) {
		this.dpiMaximo = dpiMaximo;
	}
	public String getNumMacros() {
		return numMacros;
	}
	public void setNumMacros(String numMacros) {
		this.numMacros = numMacros;
	}
	public String getPesas() {
		return pesas;
	}
	public void setPesas(String pesas) {
		this.pesas = pesas;
	}
	
	
}
