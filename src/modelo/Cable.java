package modelo;

public class Cable {
	
	private String tipoCable;
	private String versionCable;
	private int medidaCable;
	
	
	public String getTipoCable() {
		return tipoCable;
	}
	public void setTipoCable(String tipoCable) {
		this.tipoCable = tipoCable;
	}
	public String getVersionCable() {
		return versionCable;
	}
	public void setVersionCable(String versionCable) {
		this.versionCable = versionCable;
	}
	public int getMedidaCable() {
		return medidaCable;
	}
	public void setMedidaCable(int medidaCable) {
		this.medidaCable = medidaCable;
	}
	
	

}
