package modelo;

public class Fans {
	
	private int numFans;
	private String leds;
	private String tamaño;
	private String velocidadFan;
	
	
	public int getNumFans() {
		return numFans;
	}
	public void setNumFans(int numFans) {
		this.numFans = numFans;
	}
	public String getLeds() {
		return leds;
	}
	public void setLeds(String leds) {
		this.leds = leds;
	}
	public String getTamaño() {
		return tamaño;
	}
	public void setTamaño(String tamaño) {
		this.tamaño = tamaño;
	}
	public String getVelocidadFan() {
		return velocidadFan;
	}
	public void setVelocidadFan(String velocidadFan) {
		this.velocidadFan = velocidadFan;
	}
	
	

}
