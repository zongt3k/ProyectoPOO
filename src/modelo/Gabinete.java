package modelo;

public class Gabinete {
	private String tipoTapa;
	private String leds;
	private String numBahias;
	
	
	
	public String getTipoTapa() {
		return tipoTapa;
	}
	public void setTipoTapa(String tipoTapa) {
		this.tipoTapa = tipoTapa;
	}
	public String getLeds() {
		return leds;
	}
	public void setLeds(String leds) {
		this.leds = leds;
	}
	public String getNumBahias() {
		return numBahias;
	}
	public void setNumBahias(String numBahias) {
		this.numBahias = numBahias;
	}
	

	
	
}
